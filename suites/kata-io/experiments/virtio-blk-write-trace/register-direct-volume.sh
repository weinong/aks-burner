#!/usr/bin/env bash

set -Eeuo pipefail
umask 077

# Compatibility name: this is the single host-side implementation. It creates a
# loop-backed Kubernetes local block volume and never writes Kata direct-volume
# metadata.
ACTION=${1:?action is required}
RUN_ID=${2:?run ID is required}
EXPECTED_NVME_DEVICE=${3:-}
FORMAT_NVME=${4:-no}
CASE_NAME=${5:-}

BASE_DIR=/var/log/kata-virtio-blk-write-trace
RUN_DIR="$BASE_DIR/$RUN_ID"
STATE_FILE="$RUN_DIR/state.env"
COMMAND_DIR="$RUN_DIR/commands"
OWNER_DIR=/run/kata-virtio-blk-write-trace
OWNER_FILE="$OWNER_DIR/owner"
LOCK_FILE="$OWNER_DIR/lock"
MOUNTPOINT=/mnt/kata-local-nvme
BACKING_SIZE_BYTES=4294967296
BACKING_ROOT="$MOUNTPOINT/kata-virtio-blk-write-trace"
BACKING_DIR="$BACKING_ROOT/$RUN_ID"
BACKING_FILE="$BACKING_DIR/disk.raw"
HOST_OFFSETS=(8388608 67108864 536870912)
READ_OFFSETS=(16777216 134217728 1073741824)
WRITE_OFFSETS=(268435456 402653184 805306368 1610612736 2147483648)
TRACE_DIR="$RUN_DIR/tracing"
TRACE_READY="$TRACE_DIR/ready"
TRACE_STOP="$TRACE_DIR/stop"
RUN_CMD_ECHO=${RUN_CMD_ECHO:-0}
WRITE_STATE_FILE="$RUN_DIR/write-state.tsv"

timestamp() { date -u +%Y-%m-%dT%H:%M:%S.%NZ; }
die() { printf 'SAFETY_FAILURE: %s\n' "$*" >&2; exit 1; }

run_cmd() {
  local label=$1
  shift
  local start end rc stem
  start=$(timestamp)
  stem="$COMMAND_DIR/${start//[:.]/_}-$$-${label//[^A-Za-z0-9_.-]/_}"
  mkdir -p "$COMMAND_DIR"
  {
    printf 'COMMAND_START label=%q time=%s command=' "$label" "$start"
    printf '%q ' "$@"
    printf '\n'
  } >"$stem.meta"
  if "$@" >"$stem.stdout" 2>"$stem.stderr"; then rc=0; else rc=$?; fi
  [[ $RUN_CMD_ECHO != 1 ]] || cat "$stem.stdout"
  cat "$stem.stderr" >&2
  end=$(timestamp)
  printf 'COMMAND_END label=%q time=%s status=%d stdout=%q stderr=%q\n' \
    "$label" "$end" "$rc" "$stem.stdout" "$stem.stderr" >>"$stem.meta"
  cat "$stem.meta" >>"$RUN_DIR/commands.log"
  LAST_STDOUT="$stem.stdout"
  LAST_STDERR="$stem.stderr"
  return "$rc"
}

require_tools() {
  local tool
  for tool in "$@"; do
    command -v "$tool" >/dev/null 2>&1 || die "host tool is missing: $tool"
  done
}

state_value() { printf '%s=%q\n' "$1" "${2-}"; }

save_state() {
  local tmp="$STATE_FILE.tmp.$$"
  {
    state_value PHASE "${PHASE:-unknown}"
    state_value NVME_DEVICE "${NVME_DEVICE:-}"
    state_value NVME_MAJMIN "${NVME_MAJMIN:-}"
    state_value RUN_MOUNTED_NVME "${RUN_MOUNTED_NVME:-0}"
    state_value LOOP_DEVICE "${LOOP_DEVICE:-}"
    state_value LOOP_MAJMIN "${LOOP_MAJMIN:-}"
    state_value KATA_RUNTIME "${KATA_RUNTIME:-}"
    state_value KATA_CONFIG "${KATA_CONFIG:-}"
    state_value CONFIG_BACKUP "${CONFIG_BACKUP:-}"
    state_value CONFIG_HASH_ORIGINAL "${CONFIG_HASH_ORIGINAL:-}"
    state_value CONFIG_HASH_MODIFIED "${CONFIG_HASH_MODIFIED:-}"
    state_value CONFIG_CHANGED "${CONFIG_CHANGED:-0}"
    state_value CONFIG_RESTART_REQUIRED "${CONFIG_RESTART_REQUIRED:-0}"
  } >"$tmp"
  chmod 0600 "$tmp"
  mv -f "$tmp" "$STATE_FILE"
}

load_state() {
  [[ -f $STATE_FILE && $(stat -c %u "$STATE_FILE") -eq 0 ]] || die "trusted state is missing: $STATE_FILE"
  # Generated by save_state with shell-escaped values and root-only permissions.
  source "$STATE_FILE"
}

validate_common() {
  [[ $EUID -eq 0 ]] || die 'must run as root in the host mount namespace'
  [[ $RUN_ID =~ ^[a-z0-9][a-z0-9-]{0,62}$ ]] || die "invalid run ID: $RUN_ID"
  [[ $FORMAT_NVME == yes || $FORMAT_NVME == no ]] || die 'FORMAT_NVME must be yes or no'
  mkdir -p "$OWNER_DIR"
  exec 9>"$LOCK_FILE"
  flock -n 9 || die 'another host action owns the experiment lock'
}

claim_owner() {
  local current tmp
  if [[ -f $OWNER_FILE ]]; then
    current=$(<"$OWNER_FILE")
    [[ $current == "$RUN_ID" ]] || die "node is owned by another experiment run"
    return
  fi
  tmp="$OWNER_FILE.tmp.$$"
  printf '%s\n' "$RUN_ID" >"$tmp"
  chmod 0600 "$tmp"
  mv -n "$tmp" "$OWNER_FILE" || true
  rm -f "$tmp"
  [[ -f $OWNER_FILE && $(<"$OWNER_FILE") == "$RUN_ID" ]] || die 'failed to acquire node ownership'
}

require_owner() {
  [[ -f $OWNER_FILE ]] || die 'node experiment owner is absent'
  [[ $(<"$OWNER_FILE") == "$RUN_ID" ]] || die 'node experiment owner differs from this run'
}

parent_disk() {
  local current=$1 type parent
  current=$(readlink -f "$current")
  while :; do
    type=$(lsblk -ndo TYPE "$current" 2>/dev/null | xargs)
    [[ $type == disk ]] && { printf '%s\n' "$current"; return; }
    parent=$(lsblk -ndo PKNAME "$current" 2>/dev/null | awk 'NF {print $1; exit}')
    [[ -n $parent ]] || return 1
    current="/dev/$parent"
  done
}

critical_parent_disk() {
  local path=$1 source majmin
  source=$(findmnt -T "$path" -n -o SOURCE)
  source=${source%%\[*}
  if [[ $source != /dev/* || ! -b $source ]]; then
    majmin=$(findmnt -T "$path" -n -o MAJ:MIN)
    source=$(lsblk -rno PATH,MAJ:MIN | awk -v mm="$majmin" '$2 == mm {print $1; exit}')
  fi
  [[ -n $source ]] || return 1
  parent_disk "$source"
}

assert_no_sysfs_links() {
  local kind=$1 entry
  for entry in "/sys/class/block/${NVME_DEVICE##*/}/$kind/"*; do
    [[ ! -e $entry ]] || die "$NVME_DEVICE has $kind entry ${entry##*/}"
  done
}

record_preformat_proof() {
  local descendants swaps findmnt_rc=0 fuser_rc=0
  run_cmd preformat-lsblk lsblk -a -b -O "$NVME_DEVICE"
  descendants=$(lsblk -nrpo NAME "$NVME_DEVICE" | wc -l)
  [[ $descendants -eq 1 ]] || die "$NVME_DEVICE has child devices or partitions"
  run_cmd preformat-findmnt findmnt -rn -S "$NVME_DEVICE" -o TARGET,SOURCE,FSTYPE,OPTIONS || findmnt_rc=$?
  [[ $findmnt_rc -eq 1 ]] || die "unexpected findmnt result while proving no mounts: $findmnt_rc"
  [[ ! -s $LAST_STDOUT ]] || die "$NVME_DEVICE is mounted"
  assert_no_sysfs_links holders
  assert_no_sysfs_links slaves
  run_cmd preformat-swaps cat /proc/swaps
  swaps=$(awk -v dev="$NVME_DEVICE" 'NR > 1 && $1 == dev {print}' "$LAST_STDOUT")
  [[ -z $swaps ]] || die "$NVME_DEVICE is active swap"
  run_cmd preformat-users fuser -s "$NVME_DEVICE" || fuser_rc=$?
  [[ $fuser_rc -eq 1 ]] || { [[ $fuser_rc -eq 0 ]] && die "$NVME_DEVICE has users"; die "unexpected fuser result: $fuser_rc"; }
  run_cmd preformat-signatures wipefs -n "$NVME_DEVICE" || true
  printf 'no_mounts=yes\nno_children=yes\nno_holders=yes\nno_slaves=yes\nno_swap=yes\nno_users=yes\n' \
    >"$RUN_DIR/preformat-safety-proof.txt"
}

validate_nvme_identity() {
  local type transport rota critical critical_disk
  [[ -n $EXPECTED_NVME_DEVICE ]] || die 'EXPECTED_NVME_DEVICE is required'
  [[ $EXPECTED_NVME_DEVICE =~ ^/dev/nvme[0-9]+n[0-9]+$ ]] || die 'EXPECTED_NVME_DEVICE must name an exact whole NVMe namespace'
  [[ -b $EXPECTED_NVME_DEVICE ]] || die "expected NVMe device is absent: $EXPECTED_NVME_DEVICE"
  NVME_DEVICE=$(readlink -f "$EXPECTED_NVME_DEVICE")
  [[ $NVME_DEVICE == "$EXPECTED_NVME_DEVICE" ]] || die 'EXPECTED_NVME_DEVICE must be the canonical device path'
  type=$(lsblk -ndo TYPE "$NVME_DEVICE" | xargs)
  transport=$(lsblk -ndo TRAN "$NVME_DEVICE" | xargs)
  rota=$(lsblk -ndo ROTA "$NVME_DEVICE" | xargs)
  [[ $type == disk && $transport == nvme && $rota == 0 ]] || \
    die "expected device is not a whole non-rotational NVMe disk: type=$type transport=$transport rota=$rota"
  NVME_MAJMIN=$(lsblk -ndo MAJ:MIN "$NVME_DEVICE" | xargs)
  for critical in / /var/lib/kubelet /var/lib/containerd; do
    [[ -e $critical ]] || die "critical path is absent: $critical"
    critical_disk=$(critical_parent_disk "$critical") || die "cannot resolve disk for $critical"
    [[ $critical_disk != "$NVME_DEVICE" ]] || die "$NVME_DEVICE contains critical path $critical"
    printf '%s\t%s\n' "$critical" "$critical_disk" >>"$RUN_DIR/critical-disk-proof.tsv"
  done
}

save_inventory() {
  run_cmd inventory-lsblk lsblk -a -b -O
  cp "$LAST_STDOUT" "$RUN_DIR/lsblk.txt"
  run_cmd inventory-findmnt findmnt -A -o TARGET,SOURCE,FSTYPE,OPTIONS,MAJ:MIN
  cp "$LAST_STDOUT" "$RUN_DIR/findmnt.txt"
  run_cmd inventory-losetup losetup --list --output-all
  cp "$LAST_STDOUT" "$RUN_DIR/losetup.txt"
  run_cmd inventory-swaps cat /proc/swaps
  cp "$LAST_STDOUT" "$RUN_DIR/swaps.txt"
}

resolve_kata_config() {
  local candidate
  KATA_RUNTIME=$(command -v kata-runtime || true)
  [[ -n $KATA_RUNTIME && -x $KATA_RUNTIME ]] || die 'kata-runtime is unavailable on the target node'
  run_cmd kata-env "$KATA_RUNTIME" env --json || run_cmd kata-env-text "$KATA_RUNTIME" env
  cp "$LAST_STDOUT" "$RUN_DIR/kata-env-before.txt"
  candidate=$(grep -Eo '/[^" ]*configuration[^" ]*\.toml' "$LAST_STDOUT" | head -n1 || true)
  if [[ -z $candidate ]]; then
    run_cmd kata-default-configs "$KATA_RUNTIME" --show-default-config-paths
    candidate=$(while IFS= read -r path; do [[ -f $path ]] && { printf '%s' "$path"; break; }; done <"$LAST_STDOUT")
  fi
  [[ -n $candidate ]] || die 'cannot determine the effective Kata configuration'
  KATA_CONFIG=$(readlink -f "$candidate")
  [[ -f $KATA_CONFIG && ! -L $KATA_CONFIG ]] || die "unsafe Kata config path: $KATA_CONFIG"
}

hash_file() { sha256sum "$1" | awk '{print $1}'; }

restart_containerd() {
  run_cmd containerd-restart systemctl restart containerd
  run_cmd containerd-active systemctl is-active --quiet containerd
  run_cmd containerd-state systemctl show containerd -p ActiveState -p SubState -p MainPID
  grep -qx 'ActiveState=active' "$LAST_STDOUT" || die 'containerd did not become active'
  grep -qx 'SubState=running' "$LAST_STDOUT" || die 'containerd did not become running'
}

enable_kata_block_device() {
  local true_count false_count modified install_tmp mode uid gid
  resolve_kata_config
  CONFIG_BACKUP="$RUN_DIR/kata-config.original"
  run_cmd config-backup cp --preserve=mode,ownership,timestamps "$KATA_CONFIG" "$CONFIG_BACKUP"
  CONFIG_HASH_ORIGINAL=$(hash_file "$CONFIG_BACKUP")
  true_count=$(grep -Ec '^[[:space:]]*disable_block_device_use[[:space:]]*=[[:space:]]*true([[:space:]]*(#.*)?)?$' "$CONFIG_BACKUP" || true)
  false_count=$(grep -Ec '^[[:space:]]*disable_block_device_use[[:space:]]*=[[:space:]]*false([[:space:]]*(#.*)?)?$' "$CONFIG_BACKUP" || true)
  [[ $((true_count + false_count)) -eq 1 ]] || die 'expected exactly one disable_block_device_use setting'
  CONFIG_CHANGED=0
  CONFIG_RESTART_REQUIRED=0
  if [[ $false_count -eq 1 ]]; then
    CONFIG_HASH_MODIFIED=$CONFIG_HASH_ORIGINAL
    save_state
    return
  fi
  modified="$RUN_DIR/kata-config.modified"
  awk '{if ($0 ~ /^[[:space:]]*disable_block_device_use[[:space:]]*=[[:space:]]*true([[:space:]]*(#.*)?)?$/) sub(/=[[:space:]]*true/, "= false"); print}' \
    "$CONFIG_BACKUP" >"$modified"
  CONFIG_HASH_MODIFIED=$(hash_file "$modified")
  [[ $CONFIG_HASH_MODIFIED != "$CONFIG_HASH_ORIGINAL" ]] || die 'Kata config mutation did not change the file'
  CONFIG_CHANGED=1
  CONFIG_RESTART_REQUIRED=1
  save_state
  mode=$(stat -c %a "$KATA_CONFIG")
  uid=$(stat -c %u "$KATA_CONFIG")
  gid=$(stat -c %g "$KATA_CONFIG")
  install_tmp="${KATA_CONFIG}.kata-vbw-$RUN_ID.tmp"
  run_cmd config-stage install -m "$mode" -o "$uid" -g "$gid" "$modified" "$install_tmp"
  run_cmd config-install mv -f "$install_tmp" "$KATA_CONFIG"
  [[ $(hash_file "$KATA_CONFIG") == "$CONFIG_HASH_MODIFIED" ]] || die 'installed Kata config hash is wrong'
  restart_containerd
  CONFIG_RESTART_REQUIRED=0
  save_state
}

restore_kata_config() {
  local current mode uid gid restore_tmp
  [[ -n ${KATA_CONFIG:-} && -f $KATA_CONFIG && -f ${CONFIG_BACKUP:-} ]] || die 'Kata config restore inputs are missing'
  current=$(hash_file "$KATA_CONFIG")
  if [[ ${CONFIG_CHANGED:-0} == 1 ]]; then
    if [[ $current == "$CONFIG_HASH_MODIFIED" ]]; then
      mode=$(stat -c %a "$KATA_CONFIG")
      uid=$(stat -c %u "$KATA_CONFIG")
      gid=$(stat -c %g "$KATA_CONFIG")
      restore_tmp="${KATA_CONFIG}.kata-vbw-$RUN_ID.restore"
      run_cmd config-restore-stage install -m "$mode" -o "$uid" -g "$gid" "$CONFIG_BACKUP" "$restore_tmp"
      run_cmd config-restore-commit mv -f "$restore_tmp" "$KATA_CONFIG"
      current=$(hash_file "$KATA_CONFIG")
      [[ $current == "$CONFIG_HASH_ORIGINAL" ]] || die 'restored Kata config hash differs from original'
      CONFIG_CHANGED=0
      CONFIG_RESTART_REQUIRED=1
      save_state
    elif [[ $current == "$CONFIG_HASH_ORIGINAL" ]]; then
      # Retry after interruption between the atomic restore and state update.
      CONFIG_CHANGED=0
      CONFIG_RESTART_REQUIRED=1
      save_state
    else
      die "Kata config hash is neither run-modified nor original: $current"
    fi
  else
    [[ $current == "$CONFIG_HASH_ORIGINAL" ]] || die "Kata config changed outside this run: $current"
  fi
  if [[ ${CONFIG_RESTART_REQUIRED:-0} == 1 ]]; then
    restart_containerd
    CONFIG_RESTART_REQUIRED=0
    save_state
  fi
}

assert_loop_identity() {
  local backing size
  [[ -b ${LOOP_DEVICE:-} ]] || die "loop device is absent: ${LOOP_DEVICE:-unset}"
  backing=$(losetup -n -O BACK-FILE "$LOOP_DEVICE" | xargs)
  [[ $(readlink -f "$backing") == "$BACKING_FILE" ]] || die "$LOOP_DEVICE has a different backing file"
  size=$(blockdev --getsize64 "$LOOP_DEVICE")
  [[ $size -eq $BACKING_SIZE_BYTES ]] || die "$LOOP_DEVICE has unexpected size $size"
  [[ $(lsblk -ndo MAJ:MIN "$LOOP_DEVICE" | xargs) == "$LOOP_MAJMIN" ]] || die 'loop major/minor changed'
}

recover_loop_state() {
  local mappings
  [[ -e $BACKING_FILE ]] || return 0
  mappings=$(losetup -j "$BACKING_FILE" | cut -d: -f1)
  [[ -n $mappings ]] || return 0
  [[ $(printf '%s\n' "$mappings" | awk 'NF {n++} END {print n+0}') -eq 1 ]] || die 'backing file has multiple loop mappings'
  if [[ -n ${LOOP_DEVICE:-} ]]; then
    [[ $LOOP_DEVICE == "$mappings" ]] || die 'recorded and discovered loop devices differ'
  else
    LOOP_DEVICE=$mappings
    LOOP_MAJMIN=$(lsblk -ndo MAJ:MIN "$LOOP_DEVICE" | xargs)
    save_state
  fi
  assert_loop_identity
}

pattern_byte() {
  local kind=$1 index=$2
  case "$kind:$index" in
    host:0) printf H ;; host:1) printf I ;; host:2) printf J ;;
    read:0) printf r ;; read:1) printf s ;; read:2) printf t ;;
    write:0) printf A ;; write:1) printf B ;; write:2) printf C ;; write:3) printf D ;; write:4) printf E ;;
    *) die "unknown pattern $kind/$index" ;;
  esac
}

pattern_hash() {
  local byte=$1
  head -c 4096 /dev/zero | tr '\0' "$byte" | sha256sum | cut -d' ' -f1
}

write_pattern() {
  local label=$1 byte=$2 offset=$3 file
  file="$RUN_DIR/$label.bin"
  run_cmd "$label-pattern" bash -c "head -c 4096 /dev/zero | tr '\\0' '$byte' >'$file'"
  run_cmd "$label-write" dd if="$file" of="$LOOP_DEVICE" bs=4096 count=1 seek="$((offset / 4096))" conv=notrunc,fsync oflag=direct status=none
  rm -f "$file"
}

read_hash() {
  local label=$1 offset=$2 file
  file="$RUN_DIR/$label.readback"
  run_cmd "$label-read" dd if="$LOOP_DEVICE" of="$file" bs=4096 count=1 skip="$((offset / 4096))" iflag=direct status=none
  sha256sum "$file" | awk '{print $1}'
  rm -f "$file"
}

prepare() {
  local mounted_source fs_type allocated
  [[ ! -e $RUN_DIR ]] || die "run artifact directory already exists: $RUN_DIR"
  mkdir -p "$RUN_DIR" "$COMMAND_DIR" "$TRACE_DIR"
  chmod 0700 "$RUN_DIR" "$COMMAND_DIR" "$TRACE_DIR"
  claim_owner
  PHASE=preparing
  RUN_MOUNTED_NVME=0
  LOOP_DEVICE=
  LOOP_MAJMIN=
  CONFIG_CHANGED=0
  CONFIG_RESTART_REQUIRED=0
  save_state
  : >"$RUN_DIR/critical-disk-proof.tsv"
  validate_nvme_identity
  save_state
  save_inventory

  mounted_source=$(findmnt -M "$MOUNTPOINT" -n -o SOURCE 2>/dev/null || true)
  if [[ -n $mounted_source ]]; then
    [[ $(readlink -f "$mounted_source") == "$NVME_DEVICE" ]] || die "$MOUNTPOINT is occupied by another source"
    fs_type=$(findmnt -M "$MOUNTPOINT" -n -o FSTYPE)
    [[ $fs_type == ext4 ]] || die "$MOUNTPOINT is not ext4"
  else
    record_preformat_proof
    if [[ $FORMAT_NVME == yes ]]; then
      run_cmd format-nvme mkfs.ext4 -F -L kata-local-nvme "$NVME_DEVICE"
    else
      run_cmd identify-filesystem blkid -o value -s TYPE "$NVME_DEVICE"
      [[ $(<"$LAST_STDOUT") == ext4 ]] || die 'NVMe is not ext4; set FORMAT_NVME=yes only after reviewing the safety proof'
    fi
    run_cmd create-mountpoint install -d -m 0700 "$MOUNTPOINT"
    RUN_MOUNTED_NVME=1
    save_state
    run_cmd mount-nvme mount -t ext4 -o nodev,nosuid "$NVME_DEVICE" "$MOUNTPOINT"
  fi
  [[ $(readlink -f "$(findmnt -M "$MOUNTPOINT" -n -o SOURCE)") == "$NVME_DEVICE" ]] || die 'mounted NVMe identity changed'
  [[ $(findmnt -M "$MOUNTPOINT" -n -o FSTYPE) == ext4 ]] || die 'mounted filesystem is not ext4'
  run_cmd create-backing-dir install -d -m 0700 "$BACKING_DIR"
  run_cmd preallocate-backing fallocate -l "$BACKING_SIZE_BYTES" "$BACKING_FILE"
  [[ $(stat -c %s "$BACKING_FILE") -eq $BACKING_SIZE_BYTES ]] || die 'backing file is not exactly 4 GiB'
  allocated=$(( $(stat -c %b "$BACKING_FILE") * 512 ))
  [[ $allocated -ge $BACKING_SIZE_BYTES ]] || die "backing file is sparse: $allocated bytes allocated"
  run_cmd attach-loop losetup --find --show --nooverlap "$BACKING_FILE"
  LOOP_DEVICE=$(tail -n1 "$LAST_STDOUT" | xargs)
  [[ $LOOP_DEVICE =~ ^/dev/loop[0-9]+$ ]] || die "unexpected loop device: $LOOP_DEVICE"
  LOOP_MAJMIN=$(lsblk -ndo MAJ:MIN "$LOOP_DEVICE" | xargs)
  save_state
  assert_loop_identity
  enable_kata_block_device
  PHASE=prepared
  save_state
  save_inventory
  printf 'REGISTERED_DEVICE=%s\nLOOP_MAJMIN=%s\nHOST_ARTIFACT_DIR=%s\n' "$LOOP_DEVICE" "$LOOP_MAJMIN" "$RUN_DIR"
}

prepare_with_recovery() {
  local rc=0
  if prepare; then
    return 0
  else
    rc=$?
  fi
  printf 'PREPARE_FAILED status=%s; attempting idempotent recovery\n' "$rc" >&2
  if [[ -f $STATE_FILE && -f $OWNER_FILE && $(<"$OWNER_FILE") == "$RUN_ID" ]]; then
    load_state
    if [[ -z ${LOOP_DEVICE:-} || ! -b ${LOOP_DEVICE:-/dev/null} ]]; then
      PHASE=preparing
      save_state
      cleanup || printf 'PREPARE_RECOVERY_FAILED artifacts=%s\n' "$RUN_DIR" >&2
    elif [[ ${PHASE:-} != attached ]] && ! target_has_process_fd && ! sandbox_metadata_match; then
      PHASE=preparing
      save_state
      cleanup || printf 'PREPARE_RECOVERY_FAILED artifacts=%s\n' "$RUN_DIR" >&2
    fi
  fi
  return "$rc"
}

seed() {
  local index byte offset expected actual
  load_state
  [[ $PHASE == prepared ]] || die "seed requires prepared phase, found $PHASE"
  assert_loop_identity
  printf 'kind\tindex\toffset\tsha256\n' >"$RUN_DIR/seed-patterns.tsv"
  for index in 0 1 2; do
    byte=$(pattern_byte host "$index")
    offset=${HOST_OFFSETS[$index]}
    write_pattern "host-$index" "$byte" "$offset"
    expected=$(pattern_hash "$byte")
    actual=$(read_hash "host-$index" "$offset")
    [[ $actual == "$expected" ]] || die "host seed $index failed immediate readback"
    printf 'host\t%s\t%s\t%s\n' "$index" "$offset" "$expected" >>"$RUN_DIR/seed-patterns.tsv"
  done
  printf 'index\toffset\tbaseline_sha256\tcurrent_sha256\texpected_sha256\n' >"$WRITE_STATE_FILE"
  for index in 0 1 2 3 4; do
    offset=${WRITE_OFFSETS[$index]}
    actual=$(read_hash "write-baseline-$index" "$offset")
    expected=$(pattern_hash "$(pattern_byte write "$index")")
    printf '%s\t%s\t%s\t%s\t%s\n' "$index" "$offset" "$actual" "$actual" "$expected" >>"$WRITE_STATE_FILE"
  done
  for index in 0 1 2; do
    byte=$(pattern_byte read "$index")
    offset=${READ_OFFSETS[$index]}
    write_pattern "read-$index" "$byte" "$offset"
    expected=$(pattern_hash "$byte")
    actual=$(read_hash "read-$index" "$offset")
    [[ $actual == "$expected" ]] || die "guest read marker $index failed immediate readback"
    printf 'read\t%s\t%s\t%s\n' "$index" "$offset" "$expected" >>"$RUN_DIR/seed-patterns.tsv"
  done
  run_cmd seed-flush blockdev --flushbufs "$LOOP_DEVICE"
  PHASE=seeded
  save_state
  printf 'SEEDED host=3 guest_read_markers=3 write_offsets=%s,%s,%s,%s,%s\n' "${WRITE_OFFSETS[@]}"
}

find_target_fds() {
  local output=$1 proc fd hex major minor exe cmdline starttime identity
  : >"$output"
  for proc in /proc/[0-9]*; do
    [[ -d $proc/fd ]] || continue
    for fd in "$proc"/fd/*; do
      [[ -e $fd ]] || continue
      hex=$(stat -Lc '%t:%T' "$fd" 2>/dev/null || true)
      [[ $hex == *:* ]] || continue
      major=$((16#${hex%%:*}))
      minor=$((16#${hex##*:}))
      if [[ $major -eq ${LOOP_MAJMIN%%:*} && $minor -eq ${LOOP_MAJMIN##*:} ]]; then
        exe=$(readlink "$proc/exe" 2>/dev/null || true)
        cmdline=$(tr '\0' ' ' <"$proc/cmdline" 2>/dev/null || true)
        starttime=$(awk '{print $22}' "$proc/stat" 2>/dev/null || true)
        identity="$exe $cmdline"
        printf '%s\t%s\t%s\t%s\t%s\t%s\n' "${proc##*/}" "${fd##*/}" "$starttime" "$identity" \
          "$(readlink "$fd" 2>/dev/null || true)" "$hex" >>"$output"
      fi
    done
  done
}

collect() {
  local case_dir matches pid fd flags decimal mode direct nonblock pids starttime current_start exe cmdline row_count=0
  load_state
  [[ $PHASE == seeded || $PHASE == detached || $PHASE == attached ]] || die "collect cannot run in phase $PHASE"
  [[ $CASE_NAME =~ ^[A-E]$ ]] || die 'collect requires case A-E'
  assert_loop_identity
  case_dir="$RUN_DIR/case-$CASE_NAME"
  mkdir -p "$case_dir"
  matches="$case_dir/target-fds.tsv"
  find_target_fds "$matches"
  [[ -s $matches ]] || die "no process FD has st_rdev $LOOP_MAJMIN"
  pids=$(awk -F '\t' '$4 ~ /(^|[ \/])cloud-hypervisor([ ]|$)/ {print $1}' "$matches" | sort -u)
  [[ $(printf '%s\n' "$pids" | awk 'NF {n++} END {print n+0}') -eq 1 ]] || die 'expected exactly one cloud-hypervisor PID owning the target device'
  pid=$(printf '%s\n' "$pids" | awk 'NF {print; exit}')
  starttime=$(awk -F '\t' -v pid="$pid" '$1 == pid {print $3; exit}' "$matches")
  [[ $starttime =~ ^[0-9]+$ ]] || die 'Cloud Hypervisor start time is invalid'
  exe=$(readlink "/proc/$pid/exe" 2>/dev/null || true)
  cmdline=$(tr '\0' ' ' <"/proc/$pid/cmdline" 2>/dev/null || true)
  [[ "$exe $cmdline" =~ (^|[[:space:]/])cloud-hypervisor([[:space:]]|$) ]] || die 'target PID is not Cloud Hypervisor'
  printf 'pid\tstarttime\tfd\toctal_flags\tdecimal_flags\taccess_mode\tO_DIRECT\tO_NONBLOCK\n' >"$case_dir/cloud-hypervisor-fds.tsv"
  while IFS=$'\t' read -r _ fd _ _ _ _; do
    current_start=$(awk '{print $22}' "/proc/$pid/stat" 2>/dev/null || true)
    [[ $current_start == "$starttime" ]] || die 'target PID identity changed'
    [[ -r /proc/$pid/fdinfo/$fd && -e /proc/$pid/fd/$fd ]] || die "target FD $fd disappeared"
    [[ $(stat -Lc '%t:%T' "/proc/$pid/fd/$fd") == "$(printf '%x:%x' "${LOOP_MAJMIN%%:*}" "${LOOP_MAJMIN##*:}")" ]] || die "target FD $fd device identity changed"
    flags=$(awk '/^flags:/ {print $2}' "/proc/$pid/fdinfo/$fd")
    [[ $flags =~ ^[0-7]+$ ]] || die "invalid fd flags: $flags"
    decimal=$((8#$flags))
    case $((decimal & 3)) in 0) mode=O_RDONLY ;; 1) mode=O_WRONLY ;; 2) mode=O_RDWR ;; *) mode=INVALID ;; esac
    (( (decimal & 16384) != 0 )) && direct=yes || direct=no
    (( (decimal & 2048) != 0 )) && nonblock=yes || nonblock=no
    printf '%s\t%s\t%s\t%s\t%s\t%s\t%s\t%s\n' "$pid" "$starttime" "$fd" "$flags" "$decimal" "$mode" "$direct" "$nonblock" >>"$case_dir/cloud-hypervisor-fds.tsv"
    row_count=$((row_count + 1))
  done < <(awk -F '\t' -v pid="$pid" '$1 == pid {print}' "$matches")
  [[ $row_count -gt 0 ]] || die 'no validated Cloud Hypervisor FD rows were collected'
  printf '%s\n' "$exe" >"$case_dir/ch-exe.txt"
  printf '%s\n' "$cmdline" >"$case_dir/ch-cmdline.txt"
  printf '%s\n' "$starttime" >"$case_dir/ch-starttime.txt"
  run_cmd "case-$CASE_NAME-ch-status" cp "/proc/$pid/status" "$case_dir/ch-status.txt"
  run_cmd "case-$CASE_NAME-ch-cgroup" cp "/proc/$pid/cgroup" "$case_dir/ch-cgroup.txt"
  run_cmd "case-$CASE_NAME-ch-attr-current" cp "/proc/$pid/attr/current" "$case_dir/ch-attr-current.txt"
  run_cmd "case-$CASE_NAME-ch-fd-list" ls -la "/proc/$pid/fd"
  cp "$LAST_STDOUT" "$case_dir/ch-fd-list.txt"
  run_cmd "case-$CASE_NAME-ch-fdinfo" bash -c 'for f in "$1"/fdinfo/*; do printf "=== %s ===\n" "$f"; cat "$f"; done' _ "/proc/$pid"
  cp "$LAST_STDOUT" "$case_dir/ch-fdinfo.txt"
  run_cmd "case-$CASE_NAME-target-lsblk" lsblk -a -b -O "$LOOP_DEVICE"
  cp "$LAST_STDOUT" "$case_dir/target-lsblk.txt"
  run_cmd "case-$CASE_NAME-target-read-only" blockdev --getro "$LOOP_DEVICE"
  cp "$LAST_STDOUT" "$case_dir/target-read-only.txt"
  run_cmd "case-$CASE_NAME-getenforce" bash -c 'command -v getenforce >/dev/null && getenforce || printf "unavailable\n"'
  cp "$LAST_STDOUT" "$case_dir/getenforce.txt"
  run_cmd "case-$CASE_NAME-aa-status" bash -c 'command -v aa-status >/dev/null && aa-status || printf "unavailable\n"'
  cp "$LAST_STDOUT" "$case_dir/aa-status.txt"
  [[ -f $case_dir/start-time.txt ]] || die 'case start timestamp is missing'
  run_cmd "case-$CASE_NAME-kernel-journal" journalctl --no-pager -k --since "$(<"$case_dir/start-time.txt")"
  cp "$LAST_STDOUT" "$case_dir/kernel-journal.txt"
  run_cmd "case-$CASE_NAME-containerd-journal" journalctl --no-pager -u containerd --since "$(<"$case_dir/start-time.txt")"
  cp "$LAST_STDOUT" "$case_dir/containerd-journal.txt"
  run_cmd "case-$CASE_NAME-kata-journal" journalctl --no-pager --since "$(<"$case_dir/start-time.txt")" -g 'kata|cloud-hypervisor'
  cp "$LAST_STDOUT" "$case_dir/kata-journal.txt"
  PHASE=attached
  save_state
  printf 'CH_IDENTITY case=%s pid=%s starttime=%s exe=%s target=%s majmin=%s flags=%s\n' "$CASE_NAME" "$pid" "$starttime" "$exe" "$LOOP_DEVICE" "$LOOP_MAJMIN" "$case_dir/cloud-hypervisor-fds.tsv"
}

case_start() {
  load_state
  [[ $PHASE == seeded || $PHASE == detached ]] || die "case start cannot run in phase $PHASE"
  [[ $CASE_NAME =~ ^[A-E]$ ]] || die 'case-start requires case A-E'
  mkdir -p "$RUN_DIR/case-$CASE_NAME"
  date -u +%Y-%m-%dT%H:%M:%SZ >"$RUN_DIR/case-$CASE_NAME/start-time.txt"
}

start_tracer() {
  local label=$1
  shift
  local pid start status meta arg
  meta="$TRACE_DIR/$label.command.log"
  {
    printf 'COMMAND_START label=%q time=%s command=' "$label" "$(timestamp)"
    for arg in "$@"; do printf '%q ' "$arg"; done
    printf '\nstdout=%q\nstderr=%q\n' "$TRACE_DIR/$label.stdout" "$TRACE_DIR/$label.stderr"
  } >"$meta"
  setsid "$@" >"$TRACE_DIR/$label.stdout" 2>"$TRACE_DIR/$label.stderr" &
  pid=$!
  sleep 1
  if ! kill -0 "$pid" 2>/dev/null; then
    if wait "$pid"; then status=0; else status=$?; fi
    printf 'COMMAND_END label=%q time=%s status=%s\n' "$label" "$(timestamp)" "$status" >>"$meta"
    printf '%s\tunavailable\tstartup-failed\n' "$label" >>"$TRACE_DIR/availability.tsv"
    return 1
  fi
  start=$(awk '{print $22}' "/proc/$pid/stat")
  printf '%s\t%s\t%s\t%s\n' "$pid" "$start" "$label" "$meta" >>"$TRACE_DIR/pids.tsv"
  printf '%s\tavailable\tarmed\n' "$label" >>"$TRACE_DIR/availability.tsv"
}

stop_tracers() {
  local pid start label meta current deadline state status rc=0
  [[ -f $TRACE_DIR/pids.tsv ]] || return 0
  while IFS=$'\t' read -r pid start label meta; do
    [[ $pid =~ ^[0-9]+$ && -r /proc/$pid/stat ]] || continue
    current=$(awk '{print $22}' "/proc/$pid/stat")
    if [[ $current != "$start" ]]; then
      printf 'tracer PID %s was reused; refusing to signal it\n' "$pid" >&2
      rc=1
      continue
    fi
    kill -TERM -- "-$pid" 2>/dev/null || true
  done <"$TRACE_DIR/pids.tsv"
  deadline=$((SECONDS + 20))
  while IFS=$'\t' read -r pid _ label meta; do
    while kill -0 "$pid" 2>/dev/null; do
      state=$(awk '{print $3}' "/proc/$pid/stat" 2>/dev/null || true)
      [[ $state == Z ]] && break
      if (( SECONDS >= deadline )); then
        printf 'tracer %s did not stop\n' "$label" >&2
        rc=1
        break
      fi
      sleep 1
    done
    if wait "$pid" 2>/dev/null; then status=0; else status=$?; fi
    if [[ -n $meta && -f $meta ]] && ! grep -q '^COMMAND_END ' "$meta"; then
      printf 'COMMAND_END label=%q time=%s status=%s\n' "$label" "$(timestamp)" "$status" >>"$meta"
    fi
  done <"$TRACE_DIR/pids.tsv"
  return "$rc"
}

cleanup_owned_tracefs() {
  local instance trace_root
  [[ -f $TRACE_DIR/tracefs-instance ]] || return 0
  instance=$(<"$TRACE_DIR/tracefs-instance")
  case $instance in
    /sys/kernel/tracing/instances/kata-"$RUN_ID"|/sys/kernel/debug/tracing/instances/kata-"$RUN_ID") ;;
    *) printf 'refusing unexpected tracefs instance path: %s\n' "$instance" >&2; return 1 ;;
  esac
  trace_root=${instance%/instances/*}
  [[ -d $instance ]] || { rm -f "$TRACE_DIR/tracefs-instance"; return 0; }
  printf '0\n' >"$instance/tracing_on" 2>/dev/null || true
  for event in block_rq_issue block_rq_complete block_rq_error; do
    [[ ! -e $instance/events/block/$event/enable ]] || printf '0\n' >"$instance/events/block/$event/enable" 2>/dev/null || true
  done
  rmdir "$instance"
  [[ ! -e $instance && -d $trace_root/instances ]] || return 1
  rm -f "$TRACE_DIR/tracefs-instance"
}

trace_run() {
  local case_dir trace_root instance event enabled=0 issue_event=0 terminal_event=0 issue=0 terminal=0 userspace=unavailable block=unavailable evidence=insufficient deadline
  local userspace_evidence=empty stop_rc=0 tracefs_rc=0 loop_sector=$((WRITE_OFFSETS[3] / 512))
  load_state
  [[ $CASE_NAME == D ]] || die 'userspace/block tracing is intentionally limited to case D'
  [[ $PHASE == attached ]] || die "trace requires attached phase, found $PHASE"
  rm -f "$TRACE_READY" "$TRACE_STOP"
  : >"$TRACE_DIR/pids.tsv"
  printf 'component\tavailability\tdetail\n' >"$TRACE_DIR/availability.tsv"
  case_dir="$RUN_DIR/case-D"

  if command -v strace >/dev/null 2>&1; then
    pid=$(awk 'NR == 2 {print $1}' "$case_dir/cloud-hypervisor-fds.tsv")
    if start_tracer userspace timeout 180 strace -ff -ttt -T -yy -s 256 \
      -e trace=io_uring_setup,io_uring_register,io_uring_enter,pread64,pwrite64,read,write,fsync,fdatasync -p "$pid"; then
      userspace=available
    fi
  else
    printf 'userspace\tunavailable\tstrace-not-installed\n' >>"$TRACE_DIR/availability.tsv"
  fi

  if [[ -d /sys/kernel/tracing/events/block ]]; then trace_root=/sys/kernel/tracing
  elif [[ -d /sys/kernel/debug/tracing/events/block ]]; then trace_root=/sys/kernel/debug/tracing
  else trace_root=; fi
  if [[ -n $trace_root ]]; then
    instance="$trace_root/instances/kata-$RUN_ID"
    rm -rf "$instance"
    mkdir "$instance"
    printf '%s\n' "$instance" >"$TRACE_DIR/tracefs-instance"
    printf '0\n' >"$instance/tracing_on"
    : >"$instance/trace"
    for event in block_rq_issue block_rq_complete block_rq_error; do
      if [[ -e $instance/events/block/$event/enable ]]; then
        printf '1\n' >"$instance/events/block/$event/enable"
        printf '%s\n' "$event" >>"$TRACE_DIR/block-events-enabled.txt"
        enabled=$((enabled + 1))
        [[ $event == block_rq_issue ]] && issue_event=1
        [[ $event == block_rq_complete || $event == block_rq_error ]] && terminal_event=1
      fi
    done
    if [[ $enabled -gt 0 ]]; then
      printf '1\n' >"$instance/tracing_on"
      if start_tracer block timeout 180 cat "$instance/trace_pipe"; then
        if [[ $issue_event -eq 1 && $terminal_event -eq 1 ]]; then block=available; else block=partial; fi
      fi
    else
      printf 'block\tunavailable\tno-issue-completion-error-events\n' >>"$TRACE_DIR/availability.tsv"
    fi
  else
    printf 'block\tunavailable\ttracefs-block-events-absent\n' >>"$TRACE_DIR/availability.tsv"
  fi

  printf 'userspace=%s\nblock=%s\n' "$userspace" "$block" >"$TRACE_DIR/armed.env"
  touch "$TRACE_READY"
  printf 'TRACE_READY case=D userspace=%s block=%s marker=%s\n' "$userspace" "$block" "$TRACE_READY"
  # Do not retain the flock while the controller releases and observes the write.
  flock -u 9
  deadline=$((SECONDS + 180))
  while [[ ! -e $TRACE_STOP ]]; do
    (( SECONDS < deadline )) || break
    sleep 1
  done
  stop_tracers || stop_rc=$?
  cleanup_owned_tracefs || tracefs_rc=$?
  cp "$TRACE_DIR/block.stdout" "$TRACE_DIR/block-trace.raw" 2>/dev/null || : >"$TRACE_DIR/block-trace.raw"
  awk -v loop_comma="${LOOP_MAJMIN/:/,}" -v loop_colon="$LOOP_MAJMIN" -v sector="$loop_sector" '
    /block_rq_(issue|complete|error)/ {
      dev=0; hit=0
      for (i=1; i<=NF; i++) {
        token=$i; gsub(/[:,]$/, "", token)
        if ($i == loop_comma || $i == loop_colon) dev=1
        if (dev && token ~ /^[0-9]+$/ && token == sector) hit=1
      }
      if (dev && hit) print
    }
  ' "$TRACE_DIR/block-trace.raw" >"$TRACE_DIR/block-trace.filtered"
  grep -Eq 'io_uring_(setup|register|enter)|pread64|pwrite64|read\(|write\(|fsync|fdatasync' "$TRACE_DIR/userspace.stderr" 2>/dev/null && userspace_evidence=nonempty || userspace_evidence=empty
  grep -q 'block_rq_issue' "$TRACE_DIR/block-trace.filtered" 2>/dev/null && issue=1 || true
  grep -Eq 'block_rq_(complete|error)' "$TRACE_DIR/block-trace.filtered" 2>/dev/null && terminal=1 || true
  if [[ $stop_rc -eq 0 && $tracefs_rc -eq 0 && $block == available && $issue -eq 1 && $terminal -eq 1 ]]; then
    evidence=sufficient
  fi
  printf 'TRACE_RESULT userspace_availability=%s userspace_evidence=%s block_availability=%s block_issue=%s block_completion_or_error=%s tracer_stop_rc=%s tracefs_cleanup_rc=%s evidence=%s\n' \
    "$userspace" "$userspace_evidence" "$block" "$issue" "$terminal" "$stop_rc" "$tracefs_rc" "$evidence" | tee "$TRACE_DIR/result.txt"
  [[ $stop_rc -eq 0 && $tracefs_rc -eq 0 && $evidence == sufficient ]]
}

target_has_process_fd() {
  local output="$RUN_DIR/detach-process-fds.tsv"
  find_target_fds "$output"
  [[ -s $output ]]
}

sandbox_metadata_match() {
  local root match needle
  for root in /run/vc/sbs /run/kata-containers/sandboxes /run/containerd/io.containerd.runtime.v2.task; do
    [[ -d $root ]] || continue
    for needle in "$LOOP_DEVICE" "$BACKING_FILE" "$LOOP_MAJMIN"; do
      match=$(timeout 10 grep -R -l -F -m1 "$needle" "$root" 2>/dev/null | head -n1 || true)
      [[ -z $match ]] || { printf '%s\t%s\n' "$needle" "$match" >"$RUN_DIR/detach-sandbox-metadata.txt"; return 0; }
    done
  done
  return 1
}

device_detached_now() {
  local entry
  assert_loop_identity
  ! target_has_process_fd || return 1
  ! sandbox_metadata_match || return 1
  [[ -z $(findmnt -rn -S "$LOOP_DEVICE" -o TARGET) ]] || return 1
  for entry in "/sys/class/block/${LOOP_DEVICE##*/}/holders/"* "/sys/class/block/${LOOP_DEVICE##*/}/slaves/"*; do
    [[ ! -e $entry ]] || return 1
  done
  ! awk -v dev="$LOOP_DEVICE" 'NR > 1 && $1 == dev {found=1} END {exit !found}' /proc/swaps
}

wait_detached() {
  local deadline findmnt_rc=0
  load_state
  recover_loop_state
  if [[ -z ${LOOP_DEVICE:-} || ! -b $LOOP_DEVICE ]]; then
    if [[ -e $BACKING_FILE ]] && losetup -j "$BACKING_FILE" | grep -q .; then
      die 'recorded loop path is absent but the backing file still has a loop mapping'
    fi
    PHASE=detached
    save_state
    printf 'DETACHED no-loop-device=yes\n'
    return
  fi
  deadline=$((SECONDS + 120))
  until device_detached_now; do
    (( SECONDS < deadline )) || die "device remains attached; preserving PV/PVC, config, loop, backing, mount, and owner"
    sleep 1
  done
  run_cmd detached-lsblk lsblk -a -b -O "$LOOP_DEVICE"
  run_cmd detached-findmnt findmnt -rn -S "$LOOP_DEVICE" -o TARGET,SOURCE,FSTYPE,OPTIONS || findmnt_rc=$?
  [[ $findmnt_rc -eq 1 ]] || die "unexpected findmnt result after detach: $findmnt_rc"
  [[ ! -s $LAST_STDOUT ]] || die 'mount appeared after detach proof'
  PHASE=detached
  save_state
  printf 'DETACHED majmin=%s process_fds=0 sandbox_metadata=0 mounts=0 holders=0 slaves=0 swap=0\n' "$LOOP_MAJMIN"
}

verify() {
  local index byte offset expected actual write_index write_status previous baseline state_tmp
  load_state
  [[ $PHASE == detached ]] || die "host verification requires detached phase, found $PHASE"
  [[ $CASE_NAME =~ ^[A-E]$ ]] || die 'verify requires case A-E'
  device_detached_now || die 'attachment signal returned before host verification'
  for index in 0 1 2; do
    byte=$(pattern_byte host "$index")
    offset=${HOST_OFFSETS[$index]}
    expected=$(pattern_hash "$byte")
    actual=$(read_hash "verify-$CASE_NAME-host-$index" "$offset")
    [[ $actual == "$expected" ]] || die "host seed integrity failure at $offset"
  done
  for index in 0 1 2; do
    byte=$(pattern_byte read "$index")
    offset=${READ_OFFSETS[$index]}
    expected=$(pattern_hash "$byte")
    actual=$(read_hash "verify-$CASE_NAME-read-$index" "$offset")
    [[ $actual == "$expected" ]] || die "guest read marker integrity failure at $offset"
  done
  [[ -s $WRITE_STATE_FILE ]] || die 'write-offset state is missing'
  write_index=$(( $(printf '%d' "'$CASE_NAME") - $(printf '%d' "'A") ))
  state_tmp="$WRITE_STATE_FILE.tmp.$$"
  printf 'index\toffset\tbaseline_sha256\tcurrent_sha256\texpected_sha256\n' >"$state_tmp"
  for index in 0 1 2 3 4; do
    offset=${WRITE_OFFSETS[$index]}
    baseline=$(awk -F '\t' -v i="$index" '$1 == i {print $3}' "$WRITE_STATE_FILE")
    previous=$(awk -F '\t' -v i="$index" '$1 == i {print $4}' "$WRITE_STATE_FILE")
    expected=$(awk -F '\t' -v i="$index" '$1 == i {print $5}' "$WRITE_STATE_FILE")
    [[ $baseline =~ ^[0-9a-f]{64}$ && $previous =~ ^[0-9a-f]{64}$ && $expected =~ ^[0-9a-f]{64}$ ]] || die "invalid write state row $index"
    actual=$(read_hash "verify-$CASE_NAME-write-$index" "$offset")
    if [[ $index -eq $write_index ]]; then
      if [[ $actual == "$expected" ]]; then
        write_status=match
      elif [[ $actual == "$previous" ]]; then
        write_status=mismatch
      else
        rm -f "$state_tmp"
        die "unexpected corruption at current write offset $offset"
      fi
    elif [[ $actual != "$previous" ]]; then
      rm -f "$state_tmp"
      die "wrong-offset corruption at $offset while verifying case $CASE_NAME"
    fi
    printf '%s\t%s\t%s\t%s\t%s\n' "$index" "$offset" "$baseline" "$actual" "$expected" >>"$state_tmp"
  done
  mv -f "$state_tmp" "$WRITE_STATE_FILE"
  offset=${WRITE_OFFSETS[$write_index]}
  expected=$(pattern_hash "$(pattern_byte write "$write_index")")
  actual=$(awk -F '\t' -v i="$write_index" '$1 == i {print $4}' "$WRITE_STATE_FILE")
  printf 'WRITE_VERIFY case=%s offset=%s expected_sha256=%s actual_sha256=%s status=%s\n' \
    "$CASE_NAME" "$offset" "$expected" "$actual" "$write_status" | tee "$RUN_DIR/case-$CASE_NAME/write-verification.txt"
  printf 'SEED_INTEGRITY case=%s host=match guest_read_markers=match\n' "$CASE_NAME"
}

cleanup() {
  local current_source owner tracer_rc=0 tracefs_rc=0
  if [[ ! -f $STATE_FILE ]]; then
    if [[ -f $OWNER_FILE && $(<"$OWNER_FILE") == "$RUN_ID" ]]; then die 'owner exists but run state is missing'; fi
    printf 'CLEANUP_ALREADY_COMPLETE run=%s\n' "$RUN_ID"
    return
  fi
  load_state
  if [[ ${PHASE:-} == cleaned && ! -e $OWNER_FILE ]]; then
    printf 'CLEANUP_ALREADY_COMPLETE run=%s\n' "$RUN_ID"
    return
  fi
  require_owner
  touch "$TRACE_STOP"
  stop_tracers || tracer_rc=$?
  cleanup_owned_tracefs || tracefs_rc=$?
  [[ $PHASE == preparing || $PHASE == detached || $PHASE == prepared || $PHASE == seeded ]] || \
    die "cleanup is blocked in phase $PHASE; prove detachment first"
  recover_loop_state
  if [[ -n ${LOOP_DEVICE:-} && -b $LOOP_DEVICE ]]; then
    device_detached_now || die 'device is still attached; preserving config, loop, backing, mount, and owner'
  fi
  if [[ -n ${CONFIG_HASH_ORIGINAL:-} ]]; then
    restore_kata_config
  elif [[ ${CONFIG_CHANGED:-0} == 1 || ${CONFIG_RESTART_REQUIRED:-0} == 1 ]]; then
    die 'config state is incomplete; refusing cleanup'
  fi
  if [[ -n ${LOOP_DEVICE:-} && -b $LOOP_DEVICE ]]; then
    assert_loop_identity
    run_cmd detach-loop losetup -d "$LOOP_DEVICE"
  fi
  if [[ -e $BACKING_FILE ]]; then
    [[ $(readlink -f "$BACKING_FILE") == "$BACKING_FILE" ]] || die 'backing file path is not canonical'
    run_cmd remove-backing rm -f "$BACKING_FILE"
  fi
  [[ ! -d $BACKING_DIR ]] || run_cmd remove-backing-dir rmdir "$BACKING_DIR"
  if [[ ${RUN_MOUNTED_NVME:-0} == 1 ]]; then
    current_source=$(findmnt -M "$MOUNTPOINT" -n -o SOURCE 2>/dev/null || true)
    if [[ -n $current_source ]]; then
      [[ $(readlink -f "$current_source") == "$NVME_DEVICE" ]] || die 'run-mounted NVMe identity changed'
      run_cmd unmount-nvme umount "$MOUNTPOINT"
    fi
    RUN_MOUNTED_NVME=0
    save_state
  fi
  owner=$(<"$OWNER_FILE")
  [[ $owner == "$RUN_ID" ]] || die 'owner changed before release'
  run_cmd release-owner rm -f "$OWNER_FILE"
  PHASE=cleaned
  save_state
  printf 'CLEANUP_COMPLETE run=%s artifacts=%s\n' "$RUN_ID" "$RUN_DIR"
  [[ $tracer_rc -eq 0 && $tracefs_rc -eq 0 ]] || die "cleanup completed with tracer_rc=$tracer_rc tracefs_rc=$tracefs_rc"
}

